import * as fs from 'fs';
import * as path from 'path';

import type { GodotSpriteMap } from './godot-resources.ts';

// ---------------------------------------------------------------------------
// Output types (consumed by the runtime in Phase 4)
// ---------------------------------------------------------------------------

export interface CutsceneData {
  nodes: Record<string, CutsceneNode>;
  animations: Record<string, CutsceneAnimation>;
}

export interface CutsceneNode {
  type: string;
  /** Resolved PixiJS frame or animation name (e.g. "rat-boat#0", "captain") — Sprite2D only */
  pixiTexture?: string;
  /** Static text content — Label only */
  text?: string;
  /** Font size in pixels from LabelSettings — Label only */
  fontSize?: number;
  /** Width of the label rect (offset_right − offset_left) — Label only */
  width?: number;
  /** Height of the label rect (offset_bottom − offset_top) — Label only */
  height?: number;
}

export interface CutsceneAnimation {
  /** Duration in seconds */
  length: number;
  tracks: CutsceneTrack[];
  /** Sound effects triggered at specific times during this animation */
  audioCues: CutsceneAudioCue[];
}

export interface CutsceneAudioCue {
  /** Asset manifest alias (e.g. "sounds/Cat Meow B.wav") */
  sound: string;
  /** Trigger time in seconds */
  time: number;
}

export interface CutsceneTrack {
  /** Name of the scene node this track animates */
  node: string;
  /** Godot property name, e.g. "position", "rotation", "modulate:a" */
  property: string;
  interpolation: 'nearest' | 'linear' | 'cubic';
  keyframes: CutsceneKeyframe[];
}

export interface CutsceneKeyframe {
  /** Time in seconds */
  time: number;
  /** Parsed value: {x,y} for Vector2, {r,g,b,a} for Color, number, boolean */
  value: unknown;
  /** Godot easing curve value (1 = linear) */
  transition: number;
}

// ---------------------------------------------------------------------------
// Internal parser types
// ---------------------------------------------------------------------------

interface TscnSection {
  /** Section type: "gd_scene", "ext_resource", "sub_resource", "node" */
  type: string;
  /** Attributes from the section header, still quoted */
  attrs: Record<string, string>;
  /** Key=value pairs from the section body */
  props: Map<string, string>;
}

